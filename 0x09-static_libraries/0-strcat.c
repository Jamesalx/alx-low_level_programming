#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>

 /**
 * _strcat - concatenates src to dest and ends with the null character
 * @dest: input value
 * @src: input value
 * Return: void
 */
char *_strcat(char *dest, char *src)
{
	strcat(dest, src);
	return (0);
}
